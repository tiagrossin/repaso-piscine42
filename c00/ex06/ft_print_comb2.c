#include <unistd.h>

void	ft_putnum(int n)
{
	n /= 10;
	n += '0';
	write(1, &n, 1);
	n %= 10;
	n += '0';
	write(1, &n, 1);
}

int	main(void)
{
	ft_putnum(42);
	return (0);
}
